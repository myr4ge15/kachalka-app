// Экспоненциальный backoff фонового поллинга синка. Чистые функции (без таймеров,
// сети и Dexie) — покрыты тестами. Потребитель — src/db/sync.js (startSync).
//
// Зачем: цикл поллинга бил syncNow фиксированным интервалом (POLL_FAST_MS=20 c)
// даже когда сервер/сеть стабильно отдают ошибку (проект «спит», 5xx, обрыв) —
// это лишняя нагрузка на бэкенд и разряд батареи мобильного PWA. Теперь при
// подряд идущих сбоях интервал растёт экспоненциально до потолка, а первый же
// успешный прогон сбрасывает его обратно к базовому.

// Потолок отступа: даже при затяжном сбое опрашиваем не реже раза в 5 минут,
// чтобы приложение быстро «ожило», как только сервер вернётся.
export const BACKOFF_MAX_MS = 5 * 60 * 1000

// Интервал следующего прогона: базовый × 2^failures, но не выше maxMs.
// failures = 0 (сбоев нет) → базовый интервал без изменений.
export function backoffDelay(baseMs, failures, maxMs = BACKOFF_MAX_MS) {
  const f = Number(failures)
  if (!Number.isFinite(f) || f <= 0) return baseMs
  // Ограничиваем показатель степени, чтобы 2^f не переполнил число; потолок всё
  // равно режет результат сверху.
  const factor = 2 ** Math.min(f, 20)
  return Math.min(baseMs * factor, maxMs)
}

// Новое число подряд идущих сбоев по итогу прогона:
//   ok === true  → успех, сбрасываем в 0;
//   ok === false → сбой, +1;
//   иначе (прогон пропущен — офлайн / нет сессии / уже идёт) → без изменений,
//   чтобы «пустой» пропуск не считался ни успехом, ни сбоем.
export function nextFailureCount(prev, ok) {
  const p = Number.isFinite(Number(prev)) && prev > 0 ? Number(prev) : 0
  if (ok === true) return 0
  if (ok === false) return p + 1
  return p
}
