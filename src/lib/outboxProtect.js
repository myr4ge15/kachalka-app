// Чистая логика реконсиляции pull'а: какие ЛОКАЛЬНЫЕ записи нельзя перетирать
// свежей серверной выборкой.
//
// Защищаем от перезаписи:
//   • тумбстоны (`_deleted`) — намерение удалить храним до подтверждения сервером;
//   • «грязные» (`_dirty`) записи, у которых ЕЩЁ ЕСТЬ живая (не `_dead`) операция
//     в очереди — их правки вот-вот уедут push'ем, перетирать нельзя.
//
// НЕ защищаем «грязную» запись, у которой живой операции нет (очередь умерла в
// dead-letter или пуста): её флаг `_dirty` уже никогда не снимется push'ем,
// поэтому отдаём приоритет серверу. Это самолечит «вечный» значок
// несинхронизированности (жёлтый кружок), когда сервер давно знает запись, а
// локальная копия застряла грязной (см. баг с шаблонами на iPhone).
//
// Чистая функция (без Dexie) — тестируется в node. `idOf` достаёт id записи из
// операции очереди (у tpl_outbox — `templateId`, у outbox — `workoutId`).
export function protectedFromPull(locals, ops, idOf = (o) => o.templateId) {
  const live = new Set((ops ?? []).filter((o) => !o._dead).map(idOf))
  const out = new Set()
  for (const r of locals ?? []) {
    if (r._deleted) out.add(r.id)
    else if (r._dirty && live.has(r.id)) out.add(r.id)
  }
  return out
}
