// Логика повторного показа баннера обновления PWA (v1.17.8).
//
// При registerType:'prompt' новая версия скачивается и ВИСИТ в waiting-состоянии,
// а needRefresh поднимается один раз. Если пользователь нажал «Позже», баннер
// гасится — и сам по себе больше не всплывёт: registration.update() не находит
// ничего нового (свежий SW уже скачан и ждёт), поэтому needRefresh не поднимется
// снова до перезахода/следующего деплоя. Чтобы «Позже» откладывал, а не прятал
// навсегда, помечаем время отклонения и через TTL показываем баннер повторно,
// пока обновление всё ещё ждёт.
//
// Чистая функция (без DOM/таймеров) — тестируется юнит-тестом, вызывается из
// UpdatePrompt по таймеру / при возврате на вкладку / при появлении сети.
export function shouldReshowUpdate({ hasWaiting, snoozedAt, now, ttl }) {
  if (!hasWaiting) return false        // нечего показывать — нового SW нет
  if (!snoozedAt) return false         // не откладывали — повторно не навязываем
  return now - snoozedAt >= ttl        // прошло достаточно с момента «Позже»
}
