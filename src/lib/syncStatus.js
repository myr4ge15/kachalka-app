// Чистая логика индикатора синхронизации в шапке (класс + иконка + текст). Держим её
// отдельно от sync.js/App.jsx, чтобы покрыть тестами без Dexie/сети/React.
//
// Приоритет состояний: offline → syncing → pending(в очереди) → dead(застряло) →
// netError(последний прогон упал по сети) → ok.
//
// Формат бейджа — «гибрид»: иконка есть ВСЕГДА (`icon`), а видимый `text` остаётся
// только там, где нужно действие/понятна цифра (очередь/офлайн-очередь/застряло).
// «Синхронизировано» и «идёт синхронизация» — только иконка (галочка / крутящийся
// кружок), без текста: это самые частые и наименее информативные состояния. `title` —
// полное человекочитаемое описание для hover/aria (доступность не теряем).
//
// Зачем dead: операции, провалившие ретраи, уходят в dead-letter и НЕ входят в
// `pending` (pendingCount фильтрует _dead). Раньше бейдж из-за этого показывал
// «синхронизировано», хотя карточки тренировок висели с жёлтым кружком (_dirty
// не снят, т.к. push так и не прошёл). Теперь застрявшие изменения делают бейдж
// предупреждающим («N не отпр.») — шапка согласована с кружками на карточках
// и ведёт пользователя в «Профиль», где их можно повторить/отклонить.
// netError: последний прогон синка упал по сети/серверу (напр. таймаут запроса), а
// очередь пуста (пушить нечего). Раньше в этом случае бейдж показывал зелёную галочку
// «Синхронизировано», хотя pull на самом деле не прошёл. Частый триггер — авиарежим:
// navigator.onLine остаётся true (ненадёжен даже на телефоне), запросы висят до таймаута
// (DB_TIMEOUT_MS), online=true + pending=0 → ложное «ок». Показываем ЗНАЧОК «нет сети»
// (облако) предупреждающим цветом — «связи нет» всегда выглядит облаком, а не иногда
// облаком (honest-offline) / иногда ⚠. ⚠ остаётся только у застрявших правок (dead).
export function syncBadgeState({ online, syncing, pending = 0, dead = 0, netError = false }) {
  if (!online) {
    return {
      cls: 'offline',
      icon: 'offline',
      text: pending > 0 ? String(pending) : '',
      title: pending > 0 ? `Офлайн · ${pending} в очереди` : 'Офлайн',
    }
  }
  if (syncing) return { cls: 'busy', icon: 'syncing', text: '', title: 'Синхронизация…' }
  if (pending > 0) {
    return { cls: 'busy', icon: 'pending', text: String(pending), title: `${pending} не синхронизировано` }
  }
  if (dead > 0) {
    return { cls: 'warn', icon: 'warn', text: `${dead} не отпр.`, title: `${dead} не отправлено` }
  }
  if (netError) {
    // Тот же значок «нет сети» (облако), что и у honest-офлайна, но предупреждающим
    // цветом: navigator.onLine ненадёжен (в авиарежиме остаётся true даже на телефоне),
    // поэтому «связи нет» всегда выглядит облаком, а не иногда облаком / иногда ⚠. ⚠
    // остаётся эксклюзивом застрявших изменений (dead) — там реально «нужно действие».
    return { cls: 'warn', icon: 'offline', text: '', title: 'Нет связи — проверь сеть' }
  }
  return { cls: 'ok', icon: 'ok', text: '', title: 'Синхронизировано' }
}
