// Сравнение ISO-8601 строк дат. Для ISO лексикографический порядок совпадает с
// хронологическим, поэтому простые </> корректны, быстрее и не зависят от
// локали (в отличие от String.localeCompare, который к тому же может давать
// неожиданный порядок для цифр в некоторых локалях).
const norm = (v) => v ?? ''

// По возрастанию (старые раньше).
export const cmpIsoAsc = (a, b) => {
  const x = norm(a), y = norm(b)
  return x < y ? -1 : x > y ? 1 : 0
}

// По убыванию (свежие раньше).
export const cmpIsoDesc = (a, b) => {
  const x = norm(a), y = norm(b)
  return x < y ? 1 : x > y ? -1 : 0
}
