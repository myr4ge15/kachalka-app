// Чистая логика merge-часов для реконсиляции pull одной тренировки
// (PLAN-merge-clock, slice 1). Решает судьбу ЛОКАЛЬНОЙ записи при появлении
// свежей серверной строки, опираясь на серверный монотонный updated_at.
//
//   take-server — серверная версия принимается (несинхронизированных правок нет);
//   keep-local  — наша правка не старше базиса, ждёт push (прежнее поведение —
//                 «грязную/удалённую не трогаем»);
//   conflict    — сервер изменился ПОЗЖЕ нашего базиса: кто-то правил с другого
//                 устройства. Победителя и журналирование решает вызывающий код
//                 (sync.js): выживает более позднее updated_at, проигравшая
//                 версия логируется → тихая потеря становится видимой.
//
// Времена — ISO-строки (лексикографический порядок = хронологический, см. cmp.js).
// Входы:
//   dirty           — есть несинхронизированная локальная правка (_dirty);
//   deleted         — локальный тумбстон (_deleted): намерение удалить сильнее
//                     часов → всегда keep-local (как и раньше);
//   baseUpdatedAt   — серверный updated_at на момент НАЧАЛА локальной правки
//                     (_base_updated_at). Отличает «сервер новее базиса» от
//                     «сервер показывает мою же уехавшую правку». Для локально
//                     созданной (ещё не серверной) записи базиса нет → null;
//   serverUpdatedAt — updated_at входящей серверной строки.
import { cmpIsoAsc } from './cmp.js'

export function mergeDecision({ dirty, deleted, baseUpdatedAt, serverUpdatedAt }) {
  if (deleted) return 'keep-local'
  if (!dirty) return 'take-server'
  // Нет базиса (создана локально, на сервере её ещё не было) или строка со старого
  // сервера без updated_at → сравнивать нечем, держим локальную правку (push довезёт).
  if (!baseUpdatedAt || !serverUpdatedAt) return 'keep-local'
  // Сервер не новее базиса → это наш же базис (или наша уже уехавшая правка) → keep.
  if (cmpIsoAsc(serverUpdatedAt, baseUpdatedAt) <= 0) return 'keep-local'
  // Сервер новее базиса → правка с другого устройства позже нашего форка.
  return 'conflict'
}
