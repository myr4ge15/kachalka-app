// ============================================================================
// Сериализатор асинхронных операций (мьютекс на промис-цепочке).
//
// Зачем: открытие/закрытие персональной базы (openUserDb/closeUserDb в
// src/db/local.js) меняет ОБЩЕЕ модульное состояние (`db`, `currentUserId`) и
// внутри тянет долгую миграцию несинхрон. правок со старой `gym_app`. Быстрый
// выход-вход другой учёткой во время миграции пересекал эти операции: близкая
// база закрывалась из-под работающей миграции, а та писала в уже закрытый
// инстанс → несинхронизированные правки терялись. Прогоняя все операции через
// ОДНУ цепочку, гарантируем: следующая стартует только после того, как
// предыдущая полностью завершилась (в т.ч. миграция).
//
// Чистая (без Dexie) и потому тестируемая: очередь — просто хвост промисов.
// ============================================================================

// Создать сериализатор. Возвращает функцию run(fn): ставит fn в конец очереди и
// отдаёт промис его результата. fn запустится только после завершения всех ранее
// поставленных операций (успех ИЛИ ошибка — цепочка не рвётся, следующая пойдёт).
export function createSerialQueue() {
  // Хвост цепочки. Держим его «безопасным» (проглатываем исход предыдущей), чтобы
  // ошибка одной операции не блокировала и не отклоняла последующие.
  let tail = Promise.resolve()

  return function run(fn) {
    // Результат ИМЕННО этой операции — его отдаём вызвавшему (с реальной ошибкой).
    const result = tail.then(fn, fn)
    // Двигаем хвост, гася исход, чтобы очередь жила после отклонения.
    tail = result.then(noop, noop)
    return result
  }
}

function noop() {}
