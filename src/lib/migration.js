// ============================================================================
// Чистый отбор для миграции со старой ОБЩЕЙ базы `gym_app` в персональную
// `gym_app_${userId}` (PLAN-user-isolation, MAJOR).
//
// База Dexie раньше была одна на браузер и держала записи ВСЕХ входивших на
// устройство пользователей. При переезде на персональные базы переносить с
// сервера ничего не нужно (сервер — источник правды), КРОМЕ ещё не
// синхронизированных правок: тренировок с `_dirty`/`_deleted` текущего юзера и
// их операций из очереди `outbox`. Их потеря = безвозвратная потеря данных,
// поэтому отбор вынесен в чистую (без Dexie) тестируемую функцию.
//
// NB: критерий — `_dirty || _deleted` И принадлежность user_id. Чистые (уже на
// сервере) записи не переносим — их доставит первый pull в новую базу.
// ============================================================================

// Отобрать строки, которые ОБЯЗАТЕЛЬНО надо перенести в персональную базу.
//   workouts — все строки таблицы workouts старой базы;
//   outbox   — все операции очереди тренировок старой базы;
//   userId   — чей переезд выполняется.
// Возвращает { workouts, outbox } — несинхронизированные тренировки этого юзера
// и операции очереди, ссылающиеся на них (по workoutId).
export function selectDirtyForMigration(workouts, outbox, userId) {
  const mine = (workouts ?? []).filter(
    (w) => w && w.user_id === userId && (w._dirty || w._deleted)
  )
  const ids = new Set(mine.map((w) => w.id))
  const ops = (outbox ?? []).filter((o) => o && ids.has(o.workoutId))
  return { workouts: mine, outbox: ops }
}
