// ============================================================================
// Реконсиляция удалений тренировок на pull — чистый отбор БЕЗ Dexie/сети.
//
// Задача: на pull локально удалить тренировки, которые удалили на сервере (чтобы
// удаление доехало с другого устройства), НЕ трогая:
//   - несинхронизированные локальные правки (_dirty) и тумбстоны (_deleted);
//   - только что отправленные записи (justPushed) — их могло ещё не показать
//     read-replica сервера (лаг), удалять нельзя.
//
// ПОЧЕМУ ПОЛНЫЙ НАБОР id, А НЕ ОКНО. Раньше удаления сверялись с ОКНОМ контента
// (новейшие PULL_LIMIT тренировок по performed_at) и heuristic'ой границы: запись
// старше самой старой подтянутой даты не удаляли (её просто не запрашивали).
// Побочный эффект — «зазор реконсиляции»: у пользователя с историей > лимита
// удаление тренировки СТАРШЕ окна не доезжало до других устройств никогда (окно
// её не возвращало, а граница защищала от удаления). Контент по-прежнему тянем
// окном (тяжёлый join), но набор серверных id для сверки удалений — ПОЛНЫЙ
// (дешёвый `select id`), поэтому граница окна больше не нужна.
// ============================================================================

// Отобрать id локальных тренировок, которые надо удалить локально (удалены на
// сервере). serverIds — ПОЛНЫЙ набор id тренировок пользователя на сервере
// (Set или массив); justPushed — id только что отправленных (Set или массив).
export function selectStaleWorkoutIds(locals, serverIds, justPushed) {
  const server = serverIds instanceof Set ? serverIds : new Set(serverIds ?? [])
  const pushed = justPushed instanceof Set ? justPushed : new Set(justPushed ?? [])
  const stale = []
  for (const w of locals ?? []) {
    if (!w) continue
    if (w._dirty || w._deleted) continue // несинхрон. правки/тумбстоны не трогаем
    if (server.has(w.id)) continue // ещё живёт на сервере
    if (pushed.has(w.id)) continue // только что отправили — лаг реплики, не удаляем
    stale.push(w.id)
  }
  return stale
}
